#include <memory.h>
#include <stdio.h>
#include "chip8.h"

static const char *ERR_OPEN = "Something went wrong when opening the rom";

static bool memory_initialize(cpu_t *cpu, const char *filename)
{
	FILE *fp;

	fp = fopen(filename, "rb");
	if (!fp) {
		fprintf(stderr, "%s\n", ERR_OPEN);
		return false;
	}
	fread(cpu->memory + PROG_START, sizeof(uint8_t), MEM_SIZE - PROG_START, fp);
	fclose(fp);
	return true;
}

static void font_initialize(cpu_t *cpu)
{
	uint8_t chip8_fontset[] = {
		0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
		0x20, 0x60, 0x20, 0x20, 0x70, // 1
		0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
		0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
		0x90, 0x90, 0xF0, 0x10, 0x10, // 4
		0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
		0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
		0xF0, 0x10, 0x20, 0x40, 0x40, // 7
		0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
		0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
		0xF0, 0x90, 0xF0, 0x90, 0x90, // A
		0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
		0xF0, 0x80, 0x80, 0x80, 0xF0, // C
		0xE0, 0x90, 0x90, 0x90, 0xE0, // D
		0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
		0xF0, 0x80, 0xF0, 0x80, 0x80  // F
	};
	uint8_t max = sizeof(chip8_fontset) / sizeof(uint8_t);

	for (uint8_t i = 0; i < max ; ++i) {
		cpu->memory[i] = chip8_fontset[i];
	}
}

bool cpu_initialize(cpu_t *cpu, const char *filename)
{
	memset(cpu, 0, sizeof(cpu_t));
	cpu->pc = PROG_START;
	font_initialize(cpu);
	if (memory_initialize(cpu, filename) == false)
		return false;
	return true;
}
